export const SAMPLE_PROMPT = `请在当前项目中为简历编辑器增加一个「AI 润色」功能。

开始编码前，请先阅读现有项目结构、组件库和已有的 AI 调用实现，优先复用现有基础设施。

## 背景

用户目前使用 Markdown 编写简历。很多用户的经历描述偏流水账，只罗列做了什么，看不出成果和价值，但他们又不放心把整份简历交给 AI 重写。

## 当前问题

- 用户不知道哪些句子值得改；
- 直接让 AI 重写整份简历会偏离原意；
- 用户希望保留对每一处修改的控制权。

## 核心目标

通过 AI 的方式，对简历的部分字、段做润色，不偏离主旨。AI 的产出只是建议，最终由用户确认后才写回正文。

## 核心流程

1. 用户在编辑器中选中一段文字；
2. 点击「AI 优化」，输入或选择修改要求；
3. AI 以流式方式返回建议；
4. 用户在 Diff 视图中审阅，接受后才更新正文。

## 功能要求

- 支持选区、段落、章节、全文四种优化范围；
- 提供「精简」「更专业」「强化成果」三个快捷指令；
- 建议以 Diff 形式展示新增与删除；
- 用户可以在当前建议的基础上继续调整。

## 边界与约束

- AI 的输出只能作为建议，未经用户确认不得修改正文；
- 不得虚构用户简历中不存在的经历或数据；
- 不要为这个功能引入新的后端服务，复用现有的 LLM 调用方式。

## 视觉要求

- 延续现有设计系统，不新增花哨的 AI 风格元素；
- Diff 用轻量的颜色标注，不使用大面积高亮。

## 验收标准

- 选中任意文字都能发起润色；
- 拒绝建议后正文保持原样；
- 刷新页面后简历草稿不丢失。`;
