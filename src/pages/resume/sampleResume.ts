/** 剥离历史草稿开头的系统教学注释；不以它开头（用户已改动过该区域）则原样返回。 */
export function stripLegacyTutorialComment(markdown: string): string {
  if (!markdown.startsWith(LEGACY_TUTORIAL_COMMENT)) return markdown;
  return markdown.slice(LEGACY_TUTORIAL_COMMENT.length).replace(/^\n+/, "");
}
